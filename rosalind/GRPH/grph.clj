(defn find-overlaps [dna-string dna-strings] 
  (loop [index 0 adjacent []]
    (if (= index (count dna-strings)) adjacent
      (let [other-dna-string (last (nth dna-strings index))]
        (recur (inc index) 
               (if (or (= (subs dna-string 0 3) (subs other-dna-string (- (count other-dna-string) 3)))
                       (= (subs dna-string (- (count dna-string) 3)) (subs other-dna-string 0 3)))
                   (conj adjacent (nth dna-strings index))
                   adjacent))))))

(defn build-adjadency-list [all-dna-strings]
  (loop [dna-strings all-dna-strings]
    (when (not (empty? dna-strings))
      (doseq [x (find-overlaps (last (first dna-strings)) (rest dna-strings))]
        (println (str (first (first dna-strings)) " " (first x))))
      (recur (pop dna-strings)))))

